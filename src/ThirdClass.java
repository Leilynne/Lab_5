public class ThirdClass {
    private int numberFirst;
    private int numberSecond;

    public ThirdClass() {
        this.numberFirst = 0;
        this.numberSecond = 0;
    }

    public ThirdClass(int numberFirst) {
        this.numberFirst = numberFirst;
        this.numberSecond = 0;
    }
    public ThirdClass(int numberFirst, int numberSecond) {
        this.numberFirst = numberFirst;
        this.numberSecond = numberSecond;
    }
}
